var SH_TOTAL = 4860;
var SH_CAT_ORDER = ["meat","dairy","grains","veg","fruit","extras"];
var SH_CAT_LABELS = {
  meat:   "Мясо и рыба",
  dairy:  "Молочка и яйца",
  grains: "Крупы и хлеб",
  veg:    "Овощи",
  fruit:  "Фрукты",
  extras: "Масло, орехи, специи"
};

var shBought = new Set(JSON.parse(localStorage.getItem("sh2") || "[]"));
var shCat = "all";

function shSave() {
  localStorage.setItem("sh2", JSON.stringify(Array.from(shBought)));
}

function shSum() {
  var vis = SH_ITEMS.filter(function(it) {
    var el = document.getElementById(it.id);
    return el && !el.classList.contains("hidden");
  });
  var b  = vis.filter(function(it) { return shBought.has(it.id); });
  var nb = vis.filter(function(it) { return !shBought.has(it.id); });
  var spent = b.reduce(function(s,it) { return s + it.price; }, 0);
  var rem   = nb.reduce(function(s,it) { return s + it.price; }, 0);
  var tag = function(t) { return ' <span style="font-size:13px;color:var(--muted)">' + t + "</span>"; };
  document.getElementById("sh-left").innerHTML  = nb.length + tag("позиций");
  document.getElementById("sh-done").innerHTML  = b.length  + tag("позиций");
  document.getElementById("sh-spent").innerHTML = spent     + tag("₽");
  document.getElementById("sh-rem").innerHTML   = rem       + tag("₽");
}

function shFilter() {
  var q    = (document.getElementById("sh-search").value || "").toLowerCase();
  var shop = document.getElementById("sh-shop").value || "";
  SH_ITEMS.forEach(function(it) {
    var el = document.getElementById(it.id);
    if (!el) return;
    var ok = (shCat === "all" || it.cat === shCat)
          && (!q    || it.name.toLowerCase().indexOf(q) >= 0)
          && (!shop || it.shop === shop);
    if (ok) { el.classList.remove("hidden"); }
    else    { el.classList.add("hidden"); }
  });
  document.querySelectorAll(".sh-sep").forEach(function(sep) {
    var cat = sep.dataset.sep;
    var hasVis = SH_ITEMS
      .filter(function(it) { return it.cat === cat; })
      .some(function(it) {
        var el = document.getElementById(it.id);
        return el && !el.classList.contains("hidden");
      });
    sep.style.display = hasVis ? "" : "none";
  });
  shSum();
}

function shTog(id) {
  var el = document.getElementById(id);
  if (!el) return;
  if (shBought.has(id)) {
    shBought.delete(id);
    el.classList.remove("bought");
    el.querySelector(".sh-chk").textContent = "○";
  } else {
    shBought.add(id);
    el.classList.add("bought");
    el.querySelector(".sh-chk").textContent = "✓";
  }
  shSave();
  shSum();
}

function shFilterCat(cat) {
  shCat = cat;
  document.querySelectorAll(".cat-btn").forEach(function(b) {
    if (b.dataset.cat === cat) { b.classList.add("cat-active"); }
    else { b.classList.remove("cat-active"); }
  });
  shFilter();
}

function shReset() {
  shBought.clear();
  localStorage.removeItem("sh2");
  shRender();
}

function shRender() {
  var list = document.getElementById("sh-list");
  if (!list) return;
  var html = "";
  SH_CAT_ORDER.forEach(function(cat) {
    var ci = SH_ITEMS.filter(function(it) { return it.cat === cat; });
    if (!ci.length) return;
    html += '<div class="sh-sep" data-sep="' + cat + '">'
          + SH_CAT_LABELS[cat] + "</div>";
    ci.forEach(function(it) {
      var b   = shBought.has(it.id);
      var cls = b ? " bought" : "";
      var chk = b ? "✓" : "○";
      html += '<div class="sh-row' + cls + '" id="' + it.id + '"'
            + ' onclick="shTog(this.id)">'
            + '<div class="sh-chk">' + chk + "</div>"
            + '<div class="sh-nm">' + it.name
            + '<span class="sh-tag">' + it.shop + "</span></div>"
            + '<div class="sh-qty">' + it.qty + "</div>"
            + '<div class="sh-pr">'  + it.price + " ₽</div>"
            + "</div>";
    });
  });
  list.innerHTML = html;
  shFilter();
}

// Run after DOM ready
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", shRender);
} else {
  shRender();
}
